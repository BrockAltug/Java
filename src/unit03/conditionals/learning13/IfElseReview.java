package unit03.conditionals.learning13;

public class IfElseReview {
    public static void main(String[] args) {
        //new boolean variable for subscription set to true
        boolean subscription = true;
        //if true this code will run
        if(subscription){
            System.out.println("You can enjoy the World Cup live!");
        }
        //else if its not true, this code will run
        else {
            System.out.println("You can go to selcuksportsHD.com");
        }
        //this will run after the code above
        System.out.println("Forza Morocco");
    }

}
