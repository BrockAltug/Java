package unit03.conditionals.learning12;
//practice with boolean expression true or false
public class BooleanExpressionsPractice {
    public static void main(String[] args) {
        //new variable for age
        int age = 21;
        //new boolean true or false if age is equal to or over 21
        boolean is21OrAbove;
        //checks if age is equal to or over 21
        is21OrAbove = age >= 21;
        //displays true or false based on the value of the int variable
        System.out.println(is21OrAbove);
    }
}
