package unit03.conditionals.learning12;
//practice for if else statement
public class IfElsePractice {
    public static void main(String[] args) {
        //new int variable
        int age = 17;
        //checks if the int variable is greater than or equal to 16
        if(age>=16){
            //if greater than or equal to 16
            System.out.println("You can get a driver license.");
        }else{
            //if less than or equal to 16
            System.out.println("You are not old enough to get a license.");
        }
    }
}
