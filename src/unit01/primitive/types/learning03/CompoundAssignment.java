package unit01.primitive.types.learning03;

public class CompoundAssignment {

    public static void main(String[] args) {
        //practice compound variable += / *= / %=
        //value of x starts at 3
        int x = 3;
        //3 + 7 = 10
        x += 7;
        //10 * 3 = 30
        x *= 3;
        // 30 / 2 = 15
        x %= 2;
        //0 remaining
        System.out.println(x);
    }
}
