package unit01.primitive.types.learning03;

public class IncDec {

    public static void main(String[] args) {
        //Increment and decrement operators
        int x =7;
        int y = 3;
        //x = 8
        x++;
        //y = 2
        y--;
        //y = 1
        y--;
        //will print 8
        System.out.println(x);
        //will print 1
        System.out.println(y);
    }
}
